# Define a variável 'saldo' com o valor inicial de 500
saldo = 500
# Imprime o valor atual de 'saldo', que é 500
print(saldo)

# Atualiza o valor de 'saldo' para 200
saldo = 200
# Imprime o novo valor de 'saldo', que agora é 200
print(saldo)

# Adiciona 10 ao valor atual de 'saldo' (200 + 10 = 210)
saldo += 10
# Imprime o valor atualizado de 'saldo', que é 210
print(saldo)

# Subtrai 5 do valor atual de 'saldo' (210 - 5 = 205)
saldo -= 5
# Imprime o valor atualizado de 'saldo', que é 205
print(saldo)

# Divide o valor atual de 'saldo' por 2 usando divisão inteira (205 // 2 = 102)
saldo //= 2
# Imprime o valor atualizado de 'saldo', que é 102
print(saldo)

# Divide o valor atual de 'saldo' por 2 usando divisão flutuante (102 / 2 = 51.0)
saldo /= 2
# Imprime o valor atualizado de 'saldo', que agora é 51.0
print(saldo)

# Multiplica o valor atual de 'saldo' por 10 (51.0 * 10 = 510.0)
saldo *= 10
# Imprime o valor atualizado de 'saldo', que é 510.0
print(saldo)

# Calcula o resto da divisão de 'saldo' por 4 (510.0 % 4 = 2.0)
saldo %= 4
# Imprime o valor atualizado de 'saldo', que é 2.0
print(saldo)

# Eleva 'saldo' ao quadrado (2.0 ** 2 = 4.0)
saldo **= 2
# Imprime o valor atualizado de 'saldo', que é 4.0
print(saldo)
