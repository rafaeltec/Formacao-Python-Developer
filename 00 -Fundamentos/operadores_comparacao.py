# Atribui o valor 200 à variável 'saldo'.
saldo = 200

# Atribui o valor 200 à variável 'saque'.
saque = 200

# Compara se 'saldo' é igual a 'saque' e imprime o resultado.
# True se forem iguais, False se forem diferentes.
print(saldo == saque)

# Compara se 'saldo' é diferente de 'saque' e imprime o resultado.
# True se forem diferentes, False se forem iguais.
print(saldo != saque)

# Compara se 'saldo' é maior que 'saque' e imprime o resultado.
# True se 'saldo' for maior, False se for menor ou igual.
print(saldo > saque)

# Compara se 'saldo' é maior ou igual a 'saque' e imprime o resultado.
# True se 'saldo' for maior ou igual, False se for menor.
print(saldo >= saque)

# Compara se 'saldo' é menor que 'saque' e imprime o resultado.
# True se 'saldo' for menor, False se for maior ou igual.
print(saldo < saque)

# Compara se 'saldo' é menor ou igual a 'saque' e imprime o resultado.
# True se 'saldo' for menor ou igual, False se for maior.
print(saldo <= saque)
