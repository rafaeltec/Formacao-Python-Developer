# Atribui o valor 1000 à variável 'saldo'.
saldo = 1000
# Atribui o valor 1000 à variável 'limite'.
limite = 1000

# Compara se 'saldo' é o mesmo objeto que 'limite' usando o operador 'is'.
# Retorna True se ambos forem exatamente o mesmo objeto na memória, False caso contrário.
print(saldo is limite)

# Compara se 'saldo' não é o mesmo objeto que 'limite' usando o operador 'is not'.
# Retorna True se eles não forem o mesmo objeto, False se forem.
print(saldo is not limite)
