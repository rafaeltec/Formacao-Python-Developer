# Cria um conjunto chamado 'numeros'. Em um conjunto, todos os elementos são únicos.
# Mesmo que o número 2 seja mencionado duas vezes, ele aparecerá apenas uma vez no conjunto.
numeros = {1, 2, 3, 2}

# Converte o conjunto 'numeros' em uma lista. 
# Isso é feito através da função list(), que transforma o conjunto em uma lista.
# Na lista, os elementos são ordenados conforme a ordem de inserção no conjunto.
numeros = list(numeros)

# Imprime o elemento na posição 0 da lista 'numeros'.
# Em Python, os índices de listas começam em 0, então numeros[0] é o primeiro elemento da lista.
print(numeros[0])
