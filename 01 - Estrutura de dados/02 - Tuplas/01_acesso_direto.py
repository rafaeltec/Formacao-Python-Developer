# Cria uma tupla chamada 'frutas' contendo quatro tipos de frutas.
frutas = ("maçã", "laranja", "uva", "pera",)

# Imprime o primeiro elemento da tupla 'frutas', que é "maçã".
# Em Python, a indexação começa em 0, então frutas[0] se refere ao primeiro elemento.
print(frutas[0])  # maçã

# Imprime o terceiro elemento da tupla 'frutas', que é "uva".
# frutas[2] se refere ao terceiro elemento, pois a contagem começa em 0.
print(frutas[2])  # uva
