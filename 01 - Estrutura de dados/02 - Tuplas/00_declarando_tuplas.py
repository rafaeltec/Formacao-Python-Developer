# Cria uma tupla chamada 'frutas' com três elementos: laranja, pera e uva.
frutas = (
    "laranja",
    "pera",
    "uva",
)
# Imprime a tupla 'frutas'.
print(frutas)


# Cria uma tupla chamada 'letras' a partir de uma string "python".
# Cada letra da string se torna um elemento da tupla.
letras = tuple("python")
# Imprime a tupla 'letras'.
print(letras)

# Cria uma tupla chamada 'pais' contendo um único elemento "Brasil".
# É importante notar o uso da vírgula após o único elemento para que
# o Python reconheça isso como uma tupla.
pais = ("Brasil",)   # <<<<<<<<<<<<<< atenção
# Imprime a tupla 'pais'.
print(pais)



# Cria uma tupla chamada 'numeros' a partir de uma lista [1, 2, 3, 4].
numeros = tuple([1, 2, 3, 4])
# Imprime a tupla 'numeros'.
print(numeros)


