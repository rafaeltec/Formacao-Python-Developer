# Cria uma tupla chamada 'linguagens'. Tuplas são coleções ordenadas e imutáveis.
# Esta tupla contém cinco strings, cada uma representando uma linguagem de programação.
linguagens = (
    "python",
    "js",
    "c",
    "java",
    "csharp",
)

# Utiliza a função len() para obter o número de elementos na tupla 'linguagens'.
# len(linguagens) retorna o tamanho da tupla, ou seja, quantos elementos ela contém.
print(len(linguagens))  # Resultado: 5
