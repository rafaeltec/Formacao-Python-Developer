# Cria uma tupla chamada 'frutas' contendo quatro tipos de frutas.
frutas = (
    "maçã",
    "laranja",
    "uva",
    "pera",
)

# Imprime o último elemento da tupla 'frutas' usando indexação negativa.
# O índice -1 refere-se ao último elemento da tupla, que é "pera".
print(frutas[-1])  # pera

# Imprime o terceiro elemento a partir do final da tupla 'frutas', também usando indexação negativa.
# O índice -3 refere-se ao terceiro elemento a partir do final, que é "laranja".
print(frutas[-3])  # laranja
