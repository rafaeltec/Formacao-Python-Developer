# Cria uma lista chamada 'frutas' contendo três strings: "laranja", "maça" e "uva"
frutas = ["laranja", "maca", "uva"]
# Imprime a lista 'frutas'
print(frutas)

# Redefine 'frutas' como uma lista vazia
frutas = []
# Imprime a lista 'frutas', que agora está vazia
print(frutas)

# Cria uma lista chamada 'letras' contendo cada caractere da string "python" como um elemento separado
letras = list("python")
# Imprime a lista 'letras'
print(letras)

# Cria uma lista chamada 'numeros' contendo os números de 0 a 9
numeros = list(range(10))
# Imprime a lista 'numeros'
print(numeros)

# Cria uma lista chamada 'carro' com diferentes tipos de dados, incluindo strings, um inteiro, um número de ponto flutuante e um valor booleano
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
# Imprime a lista 'carro'
print(carro)
