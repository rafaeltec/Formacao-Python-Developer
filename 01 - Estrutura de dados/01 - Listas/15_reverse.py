# Cria uma lista chamada 'linguagens' contendo strings que representam nomes de linguagens de programação.
linguagens = ["python", "js", "c", "java", "csharp"]

# Inverte a ordem dos elementos na lista 'linguagens'.
# Após esta operação, o primeiro elemento da lista se torna o último, o segundo se torna o penúltimo, e assim por diante.
linguagens.reverse()

# Imprime a lista 'linguagens' após a inversão da ordem dos seus elementos.
# O resultado esperado é a lista com a ordem dos elementos invertida, ou seja, ["csharp", "java", "c", "js", "python"].
print(linguagens)  # ["csharp", "java", "c", "js", "python"]
