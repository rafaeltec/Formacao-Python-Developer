# Cria uma lista com três elementos: um inteiro, uma string e outra lista
lista = [1, "Python", [40, 30, 20]]

# Imprime a lista criada, que neste momento contém os elementos [1, "Python", [40, 30, 20]]
print(lista)  # Saída esperada: [1, "Python", [40, 30, 20]]

# Utiliza o método clear() para remover todos os elementos da lista 'lista'
# Após esta operação, a lista 'lista' estará vazia
lista.clear()

# Imprime a lista 'lista' novamente, que agora está vazia, para confirmar que todos os elementos foram removidos
print(lista)  # Saída esperada: []
