# Cria uma lista chamada 'cores' contendo quatro strings
cores = ["vermelho", "azul", "verde", "azul"]

# Conta e imprime o número de vezes que a string "vermelho" aparece na lista 'cores'
# Neste caso, "vermelho" aparece uma vez
print(cores.count("vermelho"))  # Saída esperada: 1

# Conta e imprime o número de vezes que a string "azul" aparece na lista 'cores'
# Neste caso, "azul" aparece duas vezes
print(cores.count("azul"))  # Saída esperada: 2

# Conta e imprime o número de vezes que a string "verde" aparece na lista 'cores'
# Neste caso, "verde" aparece uma vez
print(cores.count("verde"))  # Saída esperada: 1
