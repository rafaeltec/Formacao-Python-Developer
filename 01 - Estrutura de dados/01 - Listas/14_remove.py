# Cria uma lista chamada 'linguagens' com cinco itens: "python", "js", "c", "java" e "csharp".
linguagens = ["python", "js", "c", "java", "csharp"]

# Remove o item "c" da lista 'linguagens'.
# Após esta operação, a lista 'linguagens' conterá apenas "python", "js", "java" e "csharp".
linguagens.remove("c")

# Imprime a lista 'linguagens' após a remoção do elemento "c".
# A saída esperada é ["python", "js", "java", "csharp"].
print(linguagens)  # ["python", "js", "java", "csharp"]
