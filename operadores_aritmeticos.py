# Atribui o valor 20 à variável 'produto_1'.
produto_1 = 20
# Atribui o valor 10 à variável 'produto_2'.
produto_2 = 10

# Soma os valores de 'produto_1' e 'produto_2' e imprime o resultado (30).
print(produto_1 + produto_2)
# Subtrai 'produto_2' de 'produto_1' e imprime o resultado (10).
print(produto_1 - produto_2)
# Divide 'produto_1' por 'produto_2' e imprime o resultado (2.0).
print(produto_1 / produto_2)
# Realiza a divisão inteira de 'produto_1' por 'produto_2' e imprime o resultado (2).
print(produto_1 // produto_2)
# Multiplica 'produto_1' por 'produto_2' e imprime o resultado (200).
print(produto_1 * produto_2)
# Calcula o resto da divisão de 'produto_1' por 'produto_2' e imprime o resultado (0).
print(produto_1 % produto_2)
# Eleva 'produto_1' à potência de 'produto_2' e imprime o resultado (10240000000000).
print(produto_1 ** produto_2)

